function setReminder() {
    var url = window.location.href;
    var time = Date.now();
    var daysToNotify = (function ask() {
        var n = prompt(
            'How many days would you like TrialMinder to remind you in?'
            );
        if (n === null) return n;
        return isNaN(n) || +n < 1 ? ask() : n;
    }());

    if (daysToNotify !== null) createReminder(url, time, daysToNotify,
        "defaultuser", true, "Yes");

}

function neverShowOnDomain() {
    alert(
        "OK, not showing on this domain any more. Refresh the page to hide the button.");
    var trialMinderHiddenDomains = window.localStorage.getItem(
        'trialMinderHiddenDomains') || [];
    trialMinderHiddenDomains.push(window.location.hostname);
    window.localStorage.setItem('trialMinderHiddenDomains',
        trialMinderHiddenDomains);
}

